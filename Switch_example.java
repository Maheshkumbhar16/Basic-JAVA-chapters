package practice;

public class Switch_example {
	public static void main(String args[]) {
		int num=3;
		switch (num) {
		case 1:
			System.out.println("Number is 1");
			break;
		case 2:
			System.out.println("Number is 2");
			break;
		case 3:
			System.out.println("Number is 3");
			break;
		default:
			System.out.println("Number is 0");
		}
	}
}
